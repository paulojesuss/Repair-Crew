    private void btnConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConsultarActionPerformed
        try {
            int numeroOS = Integer.parseInt(txtNumero.getText());
            OrdemDeServico osEncontrada = null;
            for (OrdemDeServico os : OrdemDeServico.OrdemDeServicos) {
                if (os.getNumeroOS() == numeroOS) {
                    osEncontrada = os;
                }
            }
            if (osEncontrada == null) {
                JOptionPane.showMessageDialog(null, "Ordem de Serviço não encontrada!");
                txtNumero.setText("");
            }
            Cliente c = osEncontrada.getCliente();
            DefaultTableModel tabelaClientes = (DefaultTableModel) tabelaCliente.getModel();
            tabelaClientes.setRowCount(0);
            tabelaClientes.addRow(new Object[]{c.getCodigo(), c.getNome(), c.getCpf(), c.getEndereco(), c.getTelefone(), c.getRg()});
            Equipamento eq = osEncontrada.getEquipamento();
            DefaultTableModel tabelaEquipamentos = (DefaultTableModel) tabelaEquipamento.getModel();
            tabelaEquipamentos.setRowCount(0);
            tabelaEquipamentos.addRow(new Object[]{eq.getNome(), eq.getMarca(), eq.getModelo(), eq.getCor(), eq.getCaracteristicas(), eq.getObservacao()});
            DefaultTableModel tabelaDados = (DefaultTableModel) tabelaDadosOS.getModel();
            tabelaDados.setRowCount(0);
            tabelaDados.addRow(new Object[]{osEncontrada.getNumeroOS(), osEncontrada.getDiagnostico(), osEncontrada.getOrcamento(), osEncontrada.getStatus(), osEncontrada.getObservacoes()});
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Número da ordem de serviço inválido! Digite um número");
            txtNumero.setText("");
        }

    }//GEN-LAST:event_btnConsultarActionPerformed

