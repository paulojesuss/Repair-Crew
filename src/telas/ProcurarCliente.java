    private void btnConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConsultarActionPerformed
        String nome = txtNome.getText();
        DefaultTableModel tabela = (DefaultTableModel) tabelaClientes.getModel();
        ControlCliente cc = new ControlCliente();

        tabela.setRowCount(0);

        if (txtCodigo.getText().isEmpty() && nome.isEmpty()) {
            for (Cliente c : cc.consultarClientePorNome("")) {
                tabela.addRow(new Object[]{c.getCodigo(), c.getNome(), c.getCpf(), c.getEndereco(), c.getTelefone(), c.getRg()});
            }
        } else if (!nome.isEmpty()) {
            if (cc.consultarClientePorNome(nome).isEmpty()) {
                JOptionPane.showMessageDialog(null, "Cliente não encontrado!");
                this.dispose();
                new CadastroCliente().setVisible(true);
            } else {
                for (Cliente c : cc.consultarClientePorNome(nome)) {
                    tabela.addRow(new Object[]{c.getCodigo(), c.getNome(), c.getCpf(), c.getEndereco(), c.getTelefone(), c.getRg()
                    });
                }
            }
            txtNome.setText("");

        } else {
            try {
                int codigo = Integer.parseInt(txtCodigo.getText());
                Cliente c = cc.consultarClientePorNumero(codigo);
                if (c != null) {
                    tabela.addRow(new Object[]{c.getCodigo(), c.getNome(), c.getCpf(), c.getEndereco(), c.getTelefone(), c.getRg()});
                    txtCodigo.setText("");
                } else {
                    JOptionPane.showMessageDialog(null, "Cliente não encontrado!");
                    this.dispose();
                    new CadastroCliente().setVisible(true);

                }
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(null, "Código inválido!");
                txtCodigo.setText("");
            }
        }
    }//GEN-LAST:event_btnConsultarActionPerformed

