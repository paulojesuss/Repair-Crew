    private void preencherCampos() {
        DefaultTableModel modeloCliente = (DefaultTableModel) tabelaCliente.getModel();
        modeloCliente.setRowCount(0);
        modeloCliente.addRow(new Object[]{ os.getCliente().getCodigo(), os.getCliente().getNome(), os.getCliente().getCpf(), os.getCliente().getEndereco(), os.getCliente().getTelefone(), os.getCliente().getRg()});
        DefaultTableModel modeloEquip = (DefaultTableModel) tabelaEquipamento.getModel();
        modeloEquip.setRowCount(0);
        modeloEquip.addRow(new Object[]{ os.getEquipamento().getNome(), os.getEquipamento().getMarca(), os.getEquipamento().getModelo(), os.getEquipamento().getCor(), os.getEquipamento().getCaracteristicas(), os.getEquipamento().getObservacao()});
        DefaultTableModel modeloOS = (DefaultTableModel) tabelaDadosOS.getModel();
        modeloOS.setRowCount(0);
        modeloOS.addRow(new Object[]{ os.getNumeroOS(), os.getDiagnostico(), os.getOrcamento(), os.getStatus(), os.getObservacoes()});
    }
