    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        int linha = tabelaOS.getSelectedRow();
        if (linha == -1) {
            JOptionPane.showMessageDialog(null, "Selecione uma OS na tabela para editar.");
        }
        int numeroOS = (int) tabelaOS.getValueAt(linha, 0); // coluna 0 = N° OS
        OrdemDeServico osSelecionada = null;
        for (OrdemDeServico os : OrdemDeServico.OrdemDeServicos) {
            if (os.getNumeroOS() == numeroOS) {
                osSelecionada = os;
            }
        }

        if (osSelecionada != null) {
            this.dispose();
            new EditarOS(osSelecionada).setVisible(true);
        } else {
            JOptionPane.showMessageDialog(null, "Erro ao localizar a OS.");
        }
    }//GEN-LAST:event_btnEditarActionPerformed

    private void btnApagarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnApagarActionPerformed
        int linha = tabelaOS.getSelectedRow();

        if (linha == -1) {
            JOptionPane.showMessageDialog(null, "Selecione uma OS na tabela para apagar.");
            return;
        }

        int numeroOS = (int) tabelaOS.getValueAt(linha, 0);

        modelo.OrdemDeServico osParaRemover = null;
        for (modelo.OrdemDeServico os : modelo.OrdemDeServico.OrdemDeServicos) {
            if (os.getNumeroOS() == numeroOS) {
                osParaRemover = os;
                break;
            }
        }

        if (osParaRemover != null) {
            int confirmacao = JOptionPane.showConfirmDialog(null, "Deseja realmente apagar essa OS?", "Confirmação", JOptionPane.YES_NO_OPTION);
            if (confirmacao == JOptionPane.YES_OPTION) {
                modelo.OrdemDeServico.OrdemDeServicos.remove(osParaRemover);
                ((DefaultTableModel) tabelaOS.getModel()).removeRow(linha);
                JOptionPane.showMessageDialog(null, "OS apagada com sucesso.");
            }
        }
    }//GEN-LAST:event_btnApagarActionPerformed

    private void btnConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConsultarActionPerformed
        int linha = tabelaOS.getSelectedRow();
        if (linha == -1) {
            JOptionPane.showMessageDialog(null, "Selecione uma OS na tabela para consultar.");
            return;
        }
        int numeroOS = (int) tabelaOS.getValueAt(linha, 0); // coluna 0 = Nº OS
        modelo.OrdemDeServico osSelecionada = null;
        for (modelo.OrdemDeServico os : modelo.OrdemDeServico.OrdemDeServicos) {
            if (os.getNumeroOS() == numeroOS) {
                osSelecionada = os;
            }
        }
        if (osSelecionada != null) {
            this.dispose();
            new ConsultarOS(osSelecionada).setVisible(true);

        } else {
            JOptionPane.showMessageDialog(null, "Erro ao localizar a Ordem de Serviço.");
        }
    }//GEN-LAST:event_btnConsultarActionPerformed

