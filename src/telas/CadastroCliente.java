    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        // TODO add your handling code here:
        
        String nome = txtNome.getText();
        String cpf = txtCpf.getText();
        String endereco = txtEndereco.getText();
        String telefone = txtTelefone.getText();
        String rg = txtRg.getText();
        if(txtCodigo.getText().isEmpty() || nome.isEmpty() || cpf.isEmpty() || endereco.isEmpty() || telefone.isEmpty() || rg.isEmpty()){
            JOptionPane.showMessageDialog(null, "Todos os dados obrigatórios devem ser preenchidos", "Dados incompletos!", JOptionPane.INFORMATION_MESSAGE);
            txtCodigo.setText("");
            txtNome.setText("");
            txtCpf.setText("");
            txtEndereco.setText("");
            txtTelefone.setText("");
            txtRg.setText("");
        }else{
            try{
                int codigo = Integer.parseInt(txtCodigo.getText());
                Cliente cliente = new Cliente(codigo, nome, cpf, endereco, telefone, rg);

                ControlCliente cc = new ControlCliente();
                cc.cadastrarCliente(cliente);
                JOptionPane.showMessageDialog(null, "Cliente salvo com sucesso!", "Cadastro realizado!", JOptionPane.INFORMATION_MESSAGE);
                txtCodigo.setText("");
                txtNome.setText("");
                txtCpf.setText("");
                txtEndereco.setText("");
                txtTelefone.setText("");
                txtRg.setText("");
            } catch (NumberFormatException e){
                JOptionPane.showMessageDialog(null, "O codigo precisa ser um numero!");
            }
            
            
            
        }
        
        
    }//GEN-LAST:event_btnSalvarActionPerformed

