    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        DefaultTableModel tabela = (DefaultTableModel) tabelaOS.getModel();
        tabela.setRowCount(0);
        Cliente clienteEncontrado = null;
        ControlCliente cc = new ControlCliente();
        if (txtCodigo.getText().isEmpty() && txtNome.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Digite o nome ou o código do cliente.");
            return;
        } else if (!txtCodigo.getText().isEmpty()) {
            try {
                int codigo = Integer.parseInt(txtCodigo.getText());
                clienteEncontrado = cc.consultarClientePorNumero(codigo);
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(null, "Código inválido.");
                return;
            }
        } else if (clienteEncontrado == null && !txtNome.getText().isEmpty()) {
            ArrayList<Cliente> clientes = cc.consultarClientePorNome(txtNome.getText());
            if (!clientes.isEmpty()) {
                clienteEncontrado = clientes.get(0);
            }
        }
        if (clienteEncontrado == null) {
            JOptionPane.showMessageDialog(null, "Cliente não encontrado.");
        }
        for (modelo.OrdemDeServico os : modelo.OrdemDeServico.OrdemDeServicos) {
            if (os.getCliente().getCodigo() == clienteEncontrado.getCodigo()) {
                tabela.addRow(new Object[]{
                    os.getNumeroOS(),
                    os.getDiagnostico(),
                    os.getOrcamento(),
                    os.getStatus(),
                    clienteEncontrado.getNome()
                });
            }
        }

        txtCodigo.setText("");
        txtNome.setText("");
    }//GEN-LAST:event_btnBuscarActionPerformed

