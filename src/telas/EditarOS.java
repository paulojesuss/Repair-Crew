    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        try {
            Equipamento eq = ordem.getEquipamento();
            eq.setNome(txtNome.getText());
            eq.setMarca(txtMarca.getText());
            eq.setModelo(txtModelo.getText());
            eq.setCor(txtCor.getText());
            eq.setCaracteristicas(txtCaracteristicas.getText());
            eq.setObservacao(txtObservacao.getText());
            ordem.setDiagnostico(txtDiagnostico.getText());
            ordem.setOrcamento(Double.parseDouble(txtOrcamento.getText()));
            ordem.setObservacoes(txtObservacoes.getText());
            JOptionPane.showMessageDialog(null, "OS alterada com sucesso.");
            this.dispose();
            new ProcurarOSNumero().setVisible(true);
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Verifique o valor do orçamento(deve ser número)");
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

