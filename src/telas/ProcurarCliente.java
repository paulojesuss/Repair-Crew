    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        int linhaSelecionada = tabelaClientes.getSelectedRow();

        if (linhaSelecionada == -1) {
            JOptionPane.showMessageDialog(null, "Selecione um cliente na tabela para editar");
        } else {
            int codigo = (int) tabelaClientes.getValueAt(linhaSelecionada, 0);

            ControlCliente cc = new ControlCliente();
            Cliente cliente = cc.consultarClientePorNumero(codigo);

            if (cliente != null) {
                this.dispose();
                new EditarCliente(cliente, this).setVisible(true);
            }
        }
    }//GEN-LAST:event_btnEditarActionPerformed

    private void btnApagarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnApagarActionPerformed
        int linhaSelecionada = tabelaClientes.getSelectedRow();

        if (linhaSelecionada == -1) {
            JOptionPane.showMessageDialog(null, "Selecione um cliente na tabela para apagar");
        } else {
            int codigo = (int) tabelaClientes.getValueAt(linhaSelecionada, 0);

            ControlCliente cc = new ControlCliente();
            Cliente cliente = cc.consultarClientePorNumero(codigo);

            if (cliente != null) {
                int confirmacao = JOptionPane.showConfirmDialog(null, "Deseja realmente excluir esse cliente?", "Confirmação", JOptionPane.YES_NO_OPTION);

                if (confirmacao == JOptionPane.YES_OPTION) {
                    cc.apagarCliente(cliente);

                    ((DefaultTableModel) tabelaClientes.getModel()).removeRow(linhaSelecionada);

                    JOptionPane.showMessageDialog(null, "Cliente apagado com sucesso.", "Sucesso", JOptionPane.INFORMATION_MESSAGE);
                }
            }

        }
    }//GEN-LAST:event_btnApagarActionPerformed

