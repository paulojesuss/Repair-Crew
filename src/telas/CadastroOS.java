    private void btnOkActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOkActionPerformed
        if (txtNome.getText().isEmpty() || txtMarca.getText().isEmpty() || txtModelo.getText().isEmpty() || txtCor.getText().isEmpty() || txtCaracteristicas.getText().isEmpty() || txtObservacao.getText().isEmpty() || txtNumeroOS.getText().isEmpty() || txtOrcamento.getText().isEmpty() || txtDiagnostico.getText().isEmpty() || txtObservacoes.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Todos os dados obrigatórios devem ser preenchidos");
            txtNome.setText("");
            txtMarca.setText("");
            txtModelo.setText("");
            txtCor.setText("");
            txtCaracteristicas.setText("");
            txtObservacao.setText("");
            txtNumeroOS.setText("");
            txtOrcamento.setText("");
            txtDiagnostico.setText("");
            txtObservacoes.setText("");
        } else {
            try {
                String nome = txtNome.getText();
                String marca = txtMarca.getText();
                String modelo = txtModelo.getText();
                String cor = txtCor.getText();
                String caracteristicas = txtCaracteristicas.getText();
                String observacao = txtObservacao.getText();
                Equipamento equipamento = new Equipamento(nome, marca, modelo, cor, caracteristicas, observacao);
                int numeroOS = Integer.parseInt(txtNumeroOS.getText());
                double orcamento = Double.parseDouble(txtOrcamento.getText());
                String diagnostico = txtDiagnostico.getText();
                String observacoes = txtObservacoes.getText();
                String status = "aguardando diagnóstico";
                Cliente clienteSelecionado = this.cliente;
                OrdemDeServico os = new OrdemDeServico(numeroOS, equipamento, clienteSelecionado, status, orcamento, diagnostico, observacoes);
                OrdemDeServico.OrdemDeServicos.add(os);
                JOptionPane.showMessageDialog(null, "Ordem de Serviço cadastrada com sucesso!");
                txtNome.setText("");
                txtMarca.setText("");
                txtModelo.setText("");
                txtCor.setText("");
                txtCaracteristicas.setText("");
                txtObservacao.setText("");
                txtNumeroOS.setText("");
                txtOrcamento.setText("");
                txtDiagnostico.setText("");
                txtObservacoes.setText("");
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(null, "Verifique o número da OS e o valor do orçamento(devem ser números)");
            }
        }
    }//GEN-LAST:event_btnOkActionPerformed

